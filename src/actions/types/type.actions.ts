import { Action } from '@ngrx/store'

import { TypeQuery, TypeError } from '@models/types/type.interface'

////////////////////////////////////////////////////////////////////////////
// TYPES ///////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////

export const GET_TYPES_REQUEST = '[Get] TYPES'
export const GET_TYPES_SUCCESS = '[Get] TYPES Success'
export const GET_TYPES_FAILURE = '[Get] TYPES Failure'

export const WIPE_TYPES = '[Wipe] TYPES'

////////////////////////////////////////////////////////////////////////////
// ACTIONS /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////

export class GetTypes implements Action {
  readonly type = GET_TYPES_REQUEST
  constructor() {}
}

export class GetTypesSuccess implements Action {
  readonly type = GET_TYPES_SUCCESS
  constructor(public payload: TypeQuery) {}
}

export class GetTypesFailure implements Action {
  readonly type = GET_TYPES_FAILURE
  constructor(public payload: TypeError) {}
}

export class WipeTypes implements Action {
  readonly type = WIPE_TYPES
  constructor() {}
}

////////////////////////////////////////////////////////////////////////////
// EXPORT //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////

export type Actions = GetTypes | GetTypesSuccess | WipeTypes | GetTypesFailure

